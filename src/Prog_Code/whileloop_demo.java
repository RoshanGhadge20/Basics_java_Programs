package Prog_Code;

public class whileloop_demo 
{

	public static void main(String[] args)
	{
		//Basic program code of while loop
		/*
		 * int i=0; while(i<10) { System.out.println(i); i++; }
		 * 
		 */		
		
		//Basic program code of do-while loop
		
		int j=0;
		do
		{
			System.out.println(j);
			j++;
		}while(j<10);
		
	}

}
